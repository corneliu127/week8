
public class Lights {

	public Lights() {		
	}
	
	public void on() {
		
		System.out.println("+--------------------------------------------------+");
		System.out.println("|                 Lights are ON!                   |");			
		System.out.println("+--------------------------------------------------+");

	}
	
	public void off() {
		
		System.out.println("+--------------------------------------------------+");
		System.out.println("|                 Lights are OFF!                  |");			
		System.out.println("+--------------------------------------------------+");
		
	}
}