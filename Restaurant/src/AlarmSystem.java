
public class AlarmSystem {

	public AlarmSystem() {		
	}
	
	public void on() {
		
		System.out.println("+--------------------------------------------------+");
		System.out.println("|              The Alarm is now ARMED              |");			
		System.out.println("+--------------------------------------------------+");

	}
	
	public void off() {
		
		System.out.println("+--------------------------------------------------+");
		System.out.println("|             The Alarm is now DISARMED            |");			
		System.out.println("+--------------------------------------------------+");
	}
}

